from PyQt4 import QtGui

class QlrFileSystemModel(QtGui.QFileSystemModel):
    def __init__(self):
        super(QlrFileSystemModel, self).__init__()
        # only show qlr file
        self.setNameFilters(["*.qlr"])
        self.setNameFilterDisables(False)
        # Files are read only
        self.setReadOnly(True)
